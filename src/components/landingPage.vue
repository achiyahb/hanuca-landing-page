<template>


  <div
      class="ma-6"
  >
    <div
        v-show="!dataSaved"
    >
      <h2
          style="padding: 1rem 0 1rem 1rem"
      >砖专 驻专 专</h2>


      <v-text-field
          label="砖"
          background-color="white"
          outlined
          required
          v-model="name"
      >
      </v-text-field>


      <v-text-field
          label="驻"
          background-color="white"
          outlined
          v-model="phoneNumber"
          required
      >
      </v-text-field>

      <v-text-field
          label=""
          background-color="white"
          outlined
          v-model="email"
      >
      </v-text-field>
      <v-text-field
          label="砖 住"
          background-color="white"
          outlined
          v-model="instituteName"
      >
      </v-text-field>

      <v-checkbox
          class="pa-0 ma-0"
          v-model="checked"
          label=" 砖专 拽 驻专住 注 专"
      ></v-checkbox>
      <div
          style="display: flex; justify-content: flex-end; padding-right: 1rem;"
      >
        <v-btn
            depressed
            color="#194d74"
            style="margin-bottom: 1rem;"
            class="white--text"
            @click="submit"
            type="submit"
            x-large

        >
          爪专 转 拽砖专!
        </v-btn>
      </div>

    </div>
    <div
        v-show="dataSaved"
    >
      <h1>
        状转 注 驻转, 拽专   专注 砖状 
      </h1>
    </div>

  </div>


</template>

<script>
import firebaseApi from "@/firebase/firebaseApi";

export default {
  name: "landingPage",
  data: () => ({
    name: "",
    phoneNumber: null,
    email: '',
    instituteName: '',
    checked: false,
    dataSaved: false
  }),
  methods: {
    async submit() {

      let leadDetails = {
        name: this.name,
        phoneNumber: this.phoneNumber,
        email: this.email,
        instituteName: this.instituteName,
        checked: this.checked
      }
      let response = await firebaseApi.writeData(leadDetails, 'leads')
      if (response.key) {
        this.dataSaved = true
      }

    }
  }
}
</script>

